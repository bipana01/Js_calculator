
let firstNumber =parseInt(prompt("Enter a first number:"))
let secondNumber = parseInt(prompt("Enter a second number:"))
let operation = prompt("Enter a operation:")


if (operation == "+") {
    console.log(firstNumber + secondNumber)
}else if (operation == "-")
{
    console.log(firstNumber - secondNumber)
}else if (operation == "*")
    {
        console.log(firstNumber * secondNumber)
    }else if (operation == "/")
        {
            console.log(firstNumber / secondNumber)
        } else if (operation == "%")
            {
                console.log(firstNumber % secondNumber)
            }
                   
    

    
    


